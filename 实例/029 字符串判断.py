

print("实例一")

str = "runoob.com"
print(str.isalnum())  #是否都是数字或字母
print(str.isalpha()) #是否都是字符
print(str.isdigit()) # 是否都是数字
print(str.islower()) #是否都是小写
print(str.isupper()) #是否都是大写
print(str.istitle()) #是否都是首字母大写，像标题
print(str.isspace()) #是否都是空白字符 \t \n \r
print("----------------------")

